def run_code_execution_example(prompt: str, model_name: str = "gemini-2.5-flash"):
    """
    Demonstrates enabling the code execution tool and parsing the multi-part response.
    """
    print(f"--- Running Code Execution with Model: {model_name} ---")
    client = genai.Client()

    # CRITICAL STEP: Configuring the GenerateContentConfig to enable the tool.
    # This tells the model it has access to the isolated Python interpreter.
    config = types.GenerateContentConfig(
        tools=[types.Tool(code_execution=types.ToolCodeExecution)]
    )

    print(f"Prompt: {prompt}\n")

    try:
        response = client.models.generate_content(
            model=model_name,
            contents=prompt,
            config=config,
        )

        # The response must be iterated over to capture all parts of the code-based reasoning process.
        print("--- Response Parts Breakdown ---")
        
        # Check if any candidates were returned
        if not response.candidates:
            print("No candidates returned.")
            return

        for part in response.candidates[0].content.parts:
            # 1. Textual explanation or final summary
            if part.text is not None:
                print(f"[MODEL TEXT]:\n{part.text}")
                print("-" * 20)

            # 2. The Python code generated by the LLM, sent to the sandbox
            if part.executable_code is not None:
                language = part.executable_code.language
                code = part.executable_code.code
                print(f"[EXECUTABLE CODE ({language})]:\n{code.strip()}")
                print("-" * 20)

            # 3. The output returned from the sandboxed environment (STDOUT/STDERR)
            if part.code_execution_result is not None:
                outcome = part.code_execution_result.outcome
                output = part.code_execution_result.output
                print(f"[CODE EXECUTION RESULT (Outcome: {outcome})]:\n{output.strip()}")
                print("-" * 20)

    except Exception as e:
        print(f"An error occurred during API call: {e}")


