# 1. Create session
session = SessionLocal()

# 2. Add data (Pending State)
new_task = Task(description="Forget to commit.")
session.add(new_task)

# 3. Query the database (CRITICAL MISTAKE HERE)
# The query runs immediately, but the INSERT command has not been sent yet.
# The database only sees data that was committed previously.
tasks_in_db = session.query(Task).all() 

# This list will NOT contain 'new_task', leading to confusion.
print(len(tasks_in_db)) 
