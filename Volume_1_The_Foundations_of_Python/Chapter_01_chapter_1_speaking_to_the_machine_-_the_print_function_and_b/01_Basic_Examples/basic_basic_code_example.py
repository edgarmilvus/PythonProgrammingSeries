# This script initializes a simple console greeter.
# It demonstrates outputting strings, variables, and calculation results.

# 1. Variable Definition: Storing the system's identity
system_name = "Python Greeter V1.0"

# 2. Output Block: Displaying introductory text
print("--- Welcome to the Console Interface ---")

# 3. Output Block: Displaying the stored variable content
print("System Initialized:", system_name)

# 4. Variable Definition: Storing numerical data
number_a = 15
number_b = 7

# 5. Calculation: Performing a simple mathematical operation
total_sum = number_a + number_b

# 6. Output Block: Displaying the result of the calculation
print("Verification Check: The sum of 15 and 7 is", total_sum)

# 7. Final Output: Providing a closing message
print("Initialization complete. Awaiting command...")
